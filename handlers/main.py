@router.callback_query(F.data == "main_menu")
async def send_main_menu(message_or_call: Message | CallbackQuery):
    """–û—Ç–ø—Ä–∞–≤–∫–∞ –≥–ª–∞–≤–Ω–æ–≥–æ –º–µ–Ω—é."""
    user_role = get_user_role(message_or_call.from_user.id)
    if user_role == "student":
        menu = student_menu  # –ú–µ–Ω—é –¥–ª—è —Å—Ç—É–¥–µ–Ω—Ç–æ–≤
    elif user_role == "tutor":
        menu = tutor_menu  # –ú–µ–Ω—é –¥–ª—è –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª–µ–π
    else:
        menu = main_menu  # –û—Å–Ω–æ–≤–Ω–æ–µ –º–µ–Ω—é –¥–ª—è –≤—Å–µ—Ö
    if isinstance(message_or_call, Message):
        await message_or_call.answer("üìö –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é. –í—ã–±–µ—Ä–∏—Ç–µ –¥–µ–π—Å—Ç–≤–∏–µ:", reply_markup=menu)
    elif isinstance(message_or_call, CallbackQuery):
        await message_or_call.message.edit_text("üìö –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é. –í—ã–±–µ—Ä–∏—Ç–µ –¥–µ–π—Å—Ç–≤–∏–µ:", reply_markup=menu)


